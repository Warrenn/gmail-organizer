def test_load_rules_from_yaml_string():
    yaml_text = """
version: 1
sender_rules:
  - id: stripe
    match:
      from_contains: stripe.com
    labels: [finance, stripe]
additive_subject_rules: []
fallback_rules:
  - id: default
    labels: [newsletters]
"""
    spec = ri.load_rules_from_string(yaml_text)
    assert spec["version"] == 1
    assert spec["sender_rules"][0]["id"] == "stripe"


def test_load_rules_rejects_unknown_version():
    yaml_text = """
version: 99
sender_rules: []
additive_subject_rules: []
fallback_rules: []
"""
    with pytest.raises(ValueError, match="version"):
        ri.load_rules_from_string(yaml_text)


def test_load_rules_rejects_missing_version():
    yaml_text = """
sender_rules: []
additive_subject_rules: []
fallback_rules: []
"""
    with pytest.raises(ValueError, match="version"):
        ri.load_rules_from_string(yaml_text)


def test_load_rules_rejects_duplicate_rule_ids():
    yaml_text = """
version: 1
sender_rules:
  - id: dup
    match: {from_contains: foo}
    labels: [a]
  - id: dup
    match: {from_contains: bar}
    labels: [b]
additive_subject_rules: []
fallback_rules: []
"""
    with pytest.raises(ValueError, match="duplicate"):
        ri.load_rules_from_string(yaml_text)


def test_load_rules_rejects_sender_rule_with_subject_operator():
    """sender_rules' match is restricted to sender operators only."""
    yaml_text = """
version: 1
sender_rules:
  - id: oops
    match:
      from_contains: stripe.com
      subject_contains: payout
    labels: [a]
additive_subject_rules: []
fallback_rules: []
"""
    with pytest.raises(ValueError, match="sender_rule"):
        ri.load_rules_from_string(yaml_text)


def test_load_rules_rejects_unknown_operator():
    yaml_text = """
version: 1
sender_rules:
  - id: oops
    match:
      from_starts_with_blue: foo
    labels: [a]
additive_subject_rules: []
fallback_rules: []
"""
    with pytest.raises(ValueError, match="unknown operator"):
        ri.load_rules_from_string(yaml_text)
